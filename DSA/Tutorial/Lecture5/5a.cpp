// Bitwise 'and', 'or', 'not', 'xor' Operator
#include <iostream>
using namespace std;
int main()
{
    int a = 4, b = 6;
    cout << "a & b(and) :" << (a & b) << endl;
    cout << "a | b(or) :" << (a | b) << endl;
    cout << " ~a (not): " << (~a) << endl;
    cout << "a ^ b(xor):" << (a ^ b) << endl;
    
}